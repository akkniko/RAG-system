def get_all_qa() -> List[Tuple[str, str, str]]:
    """Возвращает полный список (термин, вопрос, эталонный ответ)."""
    return HARDCODED_QA


class QuestionGenerator:
    def __init__(self, text: str):
        self.text = text

    def generate_all_questions(self) -> List[Dict]:
        """Возвращает список вопросов по тексту.

        В текущей реализации используется заранее подготовленный список QA.
        """
        result = []
        for idx, (_term, question, reference_answer) in enumerate(HARDCODED_QA, start=1):
            result.append({
                "id": idx,
                "question": question,
                "reference_answer": reference_answer,
            })
        return result


def sample_questions(n: int = 5) -> List[Dict]:
    """
    Возвращает n случайных вопросов в формате:
    [{"id": 1, "question": "...", "reference_answer": "..."}]
    """
    pool = list(HARDCODED_QA)
    random.shuffle(pool)
    selected = pool[:n]

    result = []
    for idx, (term, question, reference_answer) in enumerate(selected, start=1):
        result.append({
            "id": idx,
            "question": question,
            "reference_answer": reference_answer,   # нужен бэкенду для проверки
        })
    return result